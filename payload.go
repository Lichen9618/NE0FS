type payload [1024]byte

func (t *payload) setNL(data [32]byte) {
	copy(t[6:38], data[:])
}

func (t *payload) getNL() [32]byte {
	var ret [32]byte
	copy(ret[:], t[6:38])
	return ret
}

func (t *payload) setNR(data [32]byte) {
	copy(t[38:70], data[:])
}

func (t *payload) getNR() [32]byte {
	var ret [32]byte
	copy(ret[:], t[38:70])
	return ret
}

func (t *payload) setData(data []byte) {
	ret := copy(t[72:1012], data)
	binary.BigEndian.PutUint16(t[70:72], uint16(ret))
}

func (t *payload) getData() []byte {
	length := binary.BigEndian.Uint16(t[70:72])
	return t[72 : 72+length]
}

func (t *payload) hash() [32]byte {
	sum := sha256.Sum256(t[:1023])
	ret := sha256.Sum256(sum[:])
	for i, j := 0, 31; i < j; i, j = i+1, j-1 {
		ret[i], ret[j] = ret[j], ret[i]
	}
	return ret
}

func (t *payload) hex() string {
	return hex.EncodeToString(t[:])
}

func (t *payload) put() error {
	values := url.Values{
		"jsonrpc": []string{"2.0"},
		"method":  []string{"sendrawtransaction"},
		"params":  []string{"[\"" + t.hex() + "\"]"},
		"id":      []string{"1"},
	}
	addr := url.URL{
		Scheme:   "http",
		Host:     "seed2.ngd.network:10332",
		RawQuery: values.Encode(),
	}
	resp, err := http.Get(addr.String())
	if err != nil {
		return err
	}

	defer resp.Body.Close()
	decoder := json.NewDecoder(resp.Body)
	var ret struct {
		Result bool `json:"result"`
		Error  struct {
			Code int `json:"code"`
		} `json:"error"`
	}
	decoder.Decode(&ret)
	if ret.Result == false {
		if ret.Error.Code == -501 {
			return nil
		}
		return errRPC{}
	}
	return nil
}

